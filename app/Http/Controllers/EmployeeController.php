    {
         $this->validate($request, [
        'brand_name' => 'required|unique:brands'
]);

         $brand = new Brand();
         $brand->brand_name = $request->get('brand_name');
         $brand->save();
         Session::flash('flash_message', 'Record added Successfully!');
        return redirect('employee/create');
    }

